my_favourite_number = 42
print(my_favourite_number)
print(type(my_favourite_number)) # int -> integer -> whole numbers

my_favourite_number = 4.2
print(my_favourite_number)
print(type(my_favourite_number)) # float -> floating point number - decimal

my_favourite_number = "forty two"
print(my_favourite_number)
print(type(my_favourite_number)) # str -> string -> collection of characters

my_favourite_number = True   # True  False
print(my_favourite_number)
print(type(my_favourite_number)) # bool -> boolean

my_favourite_number = None
print(my_favourite_number)
print(type(my_favourite_number)) # NoneType -> null/undefined